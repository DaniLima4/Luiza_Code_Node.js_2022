
let exemplodois = new Array ("João","José","Maria");


/* remove o ultimo elemento  */
exemplodois = new Array ("João","José","Maria");
exemplodois.pop();
console.log(exemplodois);


/* remove o primeiro elemento  */
exemplodois = new Array ("João","José","Maria");
exemplodois.shift();
console.log(exemplodois);


/* remove o primeiro elemento  */
exemplodois = new Array ("João","José","Maria");
exemplodois.splice(1,1);
console.log(exemplodois);